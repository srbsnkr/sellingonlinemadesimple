/**
* @package CSVI
 * @subpackage Tables
 */
class TableUserinfos extends JTable {

	/**
	 * Table constructor
	 *
	 * @copyright
	 * @author 		RolandD
	 * @todo
	 * @see
	 * @access 		public
	 * @param
	 * @return
	 * @since 		4.0
	 */
	public function __construct($db) {
		parent::__construct('#__virtuemart_userinfos', 'virtuemart_userinfo_id', $db );
	}

	/**
	 * Check for the primary key
	 *
	 * @copyright
	 * @author 		RolandD
	 * @todo
	 * @see
	 * @access 		public
	 * @param
	 * @return
	 * @since 		4.0
	 */
	public function check() {
		if (!empty($this->virtuemart_user_id)) {
			if (empty($this->virtuemart_userinfo_id)) $this->virtuemart_userinfo_id = md5(uniqid($this->virtuemart_user_id));

			// Create a placeholder since the primary key is not auto-increment
			$db = JFactory::getDbo();
			$query = "INSERT IGNORE INTO ".$db->quoteName($this->_tbl)." (".$db->quoteName($this->_tbl_key).") VALUES (".$db->Quote($this->virtuemart_userinfo_id).")";
			$db->setQuery($query);
			if ($db->query()) return true;
			else return false;

		}
		else return false;
	}

	/**
	 * Reset the keys including primary key
	 *
	 * @copyright
	 * @author 		RolandD
	 * @todo
	 * @see
	 * @access 		public
	 * @param
	 * @return
	 * @since 		4.0
	 */
	public function reset() {
		// Get the default values for the class from the table.
		foreach ($this->getFields() as $k => $v) {
			// If the property is not private, reset it.
			if (strpos($k, '_') !== 0) {
				$this->$k = NULL;
			}
		}
	}
}
